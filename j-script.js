function test() 
{
    let elm = document.getElementById("img-a");
    let s = elm.getAttribute("src");
    localStorage.setItem("val-img", s);

    let x = document.getElementById("heading-a");
    let y = x.textContent;
    localStorage.setItem("val", y);

    return false;
}
