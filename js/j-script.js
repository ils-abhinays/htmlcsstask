function test() 
{
    var elm = document.getElementById("img-a");
    var s = elm.getAttribute("src");
    localStorage.setItem("val-img", s);

    var x = document.getElementById("heading-a");
    var y = x.textContent;
    localStorage.setItem("val", y);

    return false;
}
